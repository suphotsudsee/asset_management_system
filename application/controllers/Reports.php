            $year       = date('Y');
            $start_date = $year.'-01-01';
            $end_date   = $year.'-12-31';
        }
    
        // ‡∏Å‡∏£‡∏ì‡∏µ‡∏™‡πà‡∏á start/end ‡∏°‡∏≤ ‡πÅ‡∏ï‡πà‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ‡∏™‡πà‡∏á year
        if (!$year && ($start_date || $end_date)) {
            $base = $start_date ?: $end_date;
            $year = substr($base, 0, 4);
        }
    
        $data = [];
        $data['selected_year']  = (int)$year;
        $data['selected_month'] = $month ?: '';         // üëà ‡∏™‡πà‡∏á‡∏Ñ‡πà‡∏≤‡πÑ‡∏õ‡∏ó‡∏µ‡πà view ‡πÄ‡∏™‡∏°‡∏≠
