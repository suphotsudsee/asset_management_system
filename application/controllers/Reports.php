        // year+month -> ‡∏ä‡πà‡∏ß‡∏á‡∏Ç‡∏≠‡∏á‡πÄ‡∏î‡∏∑‡∏≠‡∏ô‡∏ô‡∏±‡πâ‡∏ô
        if ($year && $month && (!$start_date && !$end_date)) {
            $start_date = sprintf('%04d-%02d-01', (int)$year, (int)$month);
            $end_date   = date('Y-m-t', strtotime($start_date));
        }
    
        // ‡∏°‡∏µ year ‡∏≠‡∏¢‡πà‡∏≤‡∏á‡πÄ‡∏î‡∏µ‡∏¢‡∏ß -> ‡∏ó‡∏±‡πâ‡∏á‡∏õ‡∏µ
        if ($year && (!$start_date && !$end_date)) {
            $start_date = $year.'-01-01';
            $end_date   = $year.'-12-31';
        }
    
        // ‡πÑ‡∏°‡πà‡∏°‡∏µ‡∏≠‡∏∞‡πÑ‡∏£ -> ‡∏õ‡∏µ‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô
